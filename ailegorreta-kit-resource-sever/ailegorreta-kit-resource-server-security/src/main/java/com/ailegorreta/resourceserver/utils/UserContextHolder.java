package com.ailegorreta.resourceserver.utils;

import org.springframework.util.Assert;

/**
 * This is the User context holder in order to store the UserContext in the same thread an forwarded
 * (or used by the application) in any time.
 *
 * @project ailegorreta-kit-resource-server-security
 * @author rlh
 * @date June 2023
 */
public class UserContextHolder {
    private static final ThreadLocal<UserContext> userContext = new ThreadLocal<>();

    public static final UserContext getContext(){
        UserContext context = userContext.get();

        if (context == null) {
            context = createEmptyContext();
            userContext.set(context);
        }
        return userContext.get();
    }

    public static final void setContext(UserContext context) {
        Assert.notNull(context, "Only non-null UserContext instances are permitted");
        userContext.set(context);
    }

    public static final UserContext createEmptyContext(){
        return new UserContext();
    }
}
